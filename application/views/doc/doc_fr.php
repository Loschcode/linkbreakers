<p>Les variables, c'était déjà quelque chose <strong>d'avancé</strong>, mais là vous allez vous attaquer à un outil<strong> encore plus puissant</strong>. Au départ, je ne pensais même pas inclure <strong>un tel système à LinkBreakers</strong>.</p>
											<p>Pour <em>la petite histoire</em>, lors d'une entrevue avec un ami informaticien, spécialisé dans le traitement de base de données, j'ai montré ce projet et expliqué<strong> le fonctionnement </strong>des variables en considérant <strong>l'infinité de possibilités</strong> qui s'ouvrait aux gens qui utiliseraient mon moteur, il a alors mis en évidence <strong>une grosse lacune du système</strong>.</p>
											<p>Lorsqu'on cherche <strong>un avion</strong> par exemple, ce qu'insère l'utilisateur ne correspond pas forcément à<strong> l'URL retournée</strong>, il y a tout un traitement serveur, qui renvoi <strong><span class="alert-info">le signe des différentes villes</span></strong> ou pays de départ et d'arrivée, <span class="alert-info"><strong>la date</strong></span>, <strong><span class="alert-info">la date de retour</span></strong>, etc.</p>
											<p>J'ai donc eu l'idée <strong>d'agrémenter </strong>le système avec des entités, qui pourraient <strong>retourner n'importe quoi</strong>, et ceci en fonction <strong>d'arguments optionnels</strong> ou de manière autonome. Littéralement des <strong>fonctions informatiques</strong>, mais orientées pour le traitement de données en barre d'adresses. Elles sont à introduire dans le champ <span class="alert-success"><strong>URL</strong></span> et se composent de deux accolades <strong class="alert-danger">{}</strong></p>
