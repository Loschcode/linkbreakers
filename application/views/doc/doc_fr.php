										<td><h3><a name="inclure_des_variables" id="inclure_des_variables"></a></h3>
											<h3>&nbsp;</h3>
											<h3>Inclure des variables</h3>
											<p>Maintenant que vous savez <strong>créer une redirection</strong>, corsons les choses en <strong>créant une redirection</strong> qui dépendra de <strong>variables</strong>.</p>
											<p>Une variable, c'est comme une <strong>inconnue en mathématiques</strong>, mais qu'on<strong> applique à l'informatique</strong>. C'est en fait une entité que l'on place et dont la valeur changera <strong>en fonction des éléments</strong> qui l'entoure. </p>
											<p>Nous avons introduit ce concept dans<strong> LinkBreakers</strong>. Ici, une variable est détectée via le signe <span class="alert-danger"><strong>$</strong></span>. Cela veut dire que si un mot commence par un <span class="alert-danger"><strong>$</strong></span>, il sera considéré par<strong> LinkBreakers</strong> comme <strong>une variable</strong>, dont on prendra ou cherchera la valeur selon la situation. Plutôt que de rester dans le théorique, utilisons un exemple concret...</p>
											<table width="81%" border="0" align="center" class="hero-unit">
												<tr>
													<td><div align="center"><img src="../../assets/img/doc/first_variable.png" width="600"></div></td>
												</tr>
											</table>
											<p>Vous ne comprenez pas<strong> la manière de procéder</strong> ? C'est pourtant <strong>très simple</strong> ! On place a ici placé une variable <span class="alert-danger"><strong>$your_research</strong></span> dans le champ <span class="alert-success"><strong>STRING</strong></span> - attention, une variable ne fait qu'un mot, d'où l'utilisation de l'<strong>underscore</strong> (_), faites aussi attention à n'utiliser que des caractères <a href="http://fr.wikipedia.org/wiki/Caractère_alphanumérique">alphanumériques</a>. Et n'oubliez pas, le <strong>nom de la variable</strong> sera vue par ceux qui utilise le moteur si jamais elle est mise dans le guide de recherche, ne mettez donc pas <strong>n'importe quoi</strong> !</p>
											<p>En ce qui concerne le champ <strong><span class="alert-success">URL</span></strong>, nous avons replacé la même variable à <strong>une petite différence</strong> prés: le <strong><span class="alert-danger">(+)</span></strong>. A quoi correspond ce <strong>signe</strong> ? C'est en fait une manière de dire au moteur <em><span class="alert-info">remplace la seconde variable par le contenu de la première variable, et remplace les espaces par un +</span></em>. Le contenu des <strong>parenthèses</strong> aurait tout aussi bien pu être <span class="alert-danger"><strong>(%20)</strong></span> ou encore <strong><span class="alert-danger">(-)</span></strong>; en théorie<strong> tout ce que vous voulez</strong>.</p>
											<p>Oui car, dans les <strong>URL</strong> les espaces sont <strong>systématiquement</strong> convertis en <strong>autre chose</strong>, et le signe dépend des moteurs, ce sera à vous d'analyser par quel symbole les espaces sont remplacés en <strong><span class="alert-info">regardant dans l'URL </span></strong>du site après avoir effectué une recherche. Dans le cas de <strong>Google</strong>, c'est un <span class="alert-danger"><strong>+</strong></span>.</p>
											<table width="81%" border="0" align="center" class="hero-unit">
												<tr>
													<td><div align="center"><img src="../../assets/img/doc/google_meaning_of_life.png" width="482" height="47"></div></td>
												</tr>
											</table>
											<p>Le<strong> format correct</strong> d'une variable dans le champ <span class="alert-success"><strong>URL</strong></span> est donc <strong><span class="alert-danger">$nom_de_la_variable(signe_separateur)</span></strong>.</p>
											<p>Dans <strong>notre exemple</strong>, le moteur aura pour mission de restituer ce que la personne insère entre <span class="alert-danger"><em>search</em></span> et <span class="alert-danger"><em>on google</em></span> lors d'une recherche, et de le replacer correctement dans l'URL, sur laquelle il sera redirigé.</p>
											<p>Il n'y a aucune limite au niveau du <strong>nombre de variables</strong>, la seule restriction est qu'<span class="alert-info"><strong>on ne peut pas placer une variable comme premier mot de recherche.</strong></span></p>
											<p>Si vous placez <span class="alert-info"><strong>deux variables d'affilée</strong></span>, le moteur considéra que la <strong>première variable</strong> ne peut faire qu'un seul mot, ce qui paraît <strong>logique</strong>. Ainsi, vous pouvez créer des entrées telles que...</p>
											<table width="81%" border="0" align="center" class="hero-unit">
												<tr>
													<td><div align="center"><img src="../../assets/img/doc/covoiturage.png" width="500"></div></td>
												</tr>
											</table>
